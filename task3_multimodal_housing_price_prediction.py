def load_and_preprocess_image(path, img_size):
    """Load image and preprocess for EfficientNet (expects [0,255] range)."""
    img = load_img(path, target_size=(img_size, img_size))
    arr = img_to_array(img)           # shape: (H, W, 3), values 0-255
    arr = np.expand_dims(arr, axis=0) # shape: (1, H, W, 3)
    return arr  # EfficientNet handles its own internal normalization


def extract_all_features(image_dir, n_samples, img_size, feature_model, batch_size=32):
    """Extract CNN features for all images using batch processing."""
    all_features = []
    n_batches = (n_samples + batch_size - 1) // batch_size

    for batch_idx in range(n_batches):
        start = batch_idx * batch_size
        end   = min(start + batch_size, n_samples)

        batch_imgs = []
        for i in range(start, end):
            arr = load_and_preprocess_image(f"{image_dir}/house_{i}.jpg", img_size)
            batch_imgs.append(arr[0])

        batch_array = np.array(batch_imgs, dtype=np.float32)
        features    = feature_model.predict(batch_array, verbose=0)
        all_features.append(features)

        if (batch_idx + 1) % 5 == 0 or batch_idx == n_batches - 1:
            pct = (end / n_samples) * 100
            print(f"   Progress: {end}/{n_samples} images ({pct:.0f}%)")

    return np.vstack(all_features).astype(np.float32)


