"""##  Step 7: Build Multimodal Fusion Model

We build a neural network with:
- **Branch 1:** Processes image features (Dense layers)
- **Branch 2:** Processes tabular features (Dense layers)
- **Fusion Layer:** Concatenates both branches
- **Output:** Single neuron for price regression
"""

def build_multimodal_model(img_dim, tab_dim):
    """
    Build multimodal fusion model:
    - Image branch  : Dense(256) → BN → Dropout
    - Tabular branch: Dense(64)  → BN → Dropout
    - Fusion        : Concatenate → Dense(128) → Dense(64) → Output(1)
    """

    # --- Image Branch ---
    img_input = Input(shape=(img_dim,), name='image_input')
    x_img = Dense(256, activation='relu')(img_input)
    x_img = BatchNormalization()(x_img)
    x_img = Dropout(0.4)(x_img)
    x_img = Dense(128, activation='relu')(x_img)
    x_img = Dropout(0.3)(x_img)

    # --- Tabular Branch ---
    tab_input = Input(shape=(tab_dim,), name='tabular_input')
    x_tab = Dense(64, activation='relu')(tab_input)
    x_tab = BatchNormalization()(x_tab)
    x_tab = Dropout(0.3)(x_tab)
    x_tab = Dense(32, activation='relu')(x_tab)

    # --- Feature Fusion ---
    merged = Concatenate(name='feature_fusion')([x_img, x_tab])
    merged = Dense(128, activation='relu')(merged)
    merged = Dropout(0.3)(merged)
    merged = Dense(64, activation='relu')(merged)
    merged = Dense(32, activation='relu')(merged)

    # --- Output (regression) ---
    output = Dense(1, name='price_output')(merged)

    model = Model(
        inputs=[img_input, tab_input],
        outputs=output,
        name='MultimodalHousingModel'
    )
    return model


# Build model
