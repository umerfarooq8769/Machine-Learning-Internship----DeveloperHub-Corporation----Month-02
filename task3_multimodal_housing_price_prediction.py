"""##  Step 4: Generate Synthetic House Images

Since we don't have real house photos paired with this dataset, we create **synthetic images** that simulate house color tones based on price range. This is a valid approach for demonstrating the multimodal pipeline.

>  **Note:** In a real-world project, you would use actual house listing photos scraped from real estate websites.
"""

IMAGE_DIR = "house_images"
