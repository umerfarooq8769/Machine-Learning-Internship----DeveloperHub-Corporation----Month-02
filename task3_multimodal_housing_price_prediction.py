os.makedirs(IMAGE_DIR, exist_ok=True)

def create_synthetic_house_image(price, price_min, price_max, img_size=64):
    """
    Create a synthetic image that visually encodes house price:
    - Low price  → cooler tones (blue)
    - High price → warmer tones (orange/red)
    - Add subtle texture noise to simulate real photos
    """
    # Normalize price to [0, 1]
    t = (price - price_min) / (price_max - price_min + 1e-8)
    t = float(np.clip(t, 0, 1))

    # Color gradient: blue (low) → orange (high)
    r = int(50  + t * 180)
    g = int(100 + t * 80)
    b = int(200 - t * 150)

    # Base image with solid color
    img_array = np.full((img_size, img_size, 3), [r, g, b], dtype=np.uint8)

    # Add realistic noise texture
    noise = np.random.randint(-25, 25, (img_size, img_size, 3))
    img_array = np.clip(img_array.astype(int) + noise, 0, 255).astype(np.uint8)

    # Add horizontal gradient bands (simulate house structure)
    for row in range(img_size):
        band_val = int(20 * np.sin(row * np.pi / img_size))
        img_array[row] = np.clip(img_array[row].astype(int) + band_val, 0, 255).astype(np.uint8)

    return Image.fromarray(img_array)


