"""##  Step 5: CNN Feature Extraction

We use **EfficientNetB0** (pretrained on ImageNet) as a frozen feature extractor.
- `include_top=False` → removes the classification head
- `GlobalAveragePooling2D` → converts feature maps to a flat vector
- We **freeze** all weights (transfer learning — no retraining of CNN)
"""

print(" Loading pretrained EfficientNetB0...")

# Build feature extractor (frozen)
base_model = EfficientNetB0(
    weights='imagenet',
    include_top=False,
    input_shape=(IMG_SIZE, IMG_SIZE, 3)
)
